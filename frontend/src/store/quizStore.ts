const IS_DEV = import.meta.env.DEV === true;

const MAX_RETRIES = 3;
const RETRY_DELAY_MS = 1500;
// AC-FE-PERF-POLL-2 (Phase 7): hard cap on any single re-poll delay so a
// hostile or buggy upstream cannot strand the FE for minutes.
const MAX_POLL_DELAY_MS = 10_000;
const MIN_SESSION_PERSIST_INTERVAL_MS = 1000;

/**
 * AC-FE-PERF-POLL-1 — clamp a re-poll delay to [base, MAX_POLL_DELAY_MS].
 * Honours `retryAfterMs` from the server (e.g. 429 Retry-After) but never
 * exceeds the hard cap.
 */
function clampPollDelay(baseMs: number, retryAfterMs?: number | null): number {
  const candidate = Math.max(baseMs, retryAfterMs ?? 0);
  return Math.min(MAX_POLL_DELAY_MS, Math.max(0, candidate));
}

/**
 * AC-FE-RELY-POLL-2 — exponential backoff with jitter for consecutive
 * 5xx / network failures. The next delay is
 *   ``min(MAX_POLL_DELAY_MS, baseMs * 2 ** (streak - 1)) + jitter[0, 250)``
 * Server `Retry-After` (passed via ``retryAfterMs``) always wins over the
 * computed backoff (still subject to the MAX clamp).
 */
function failureBackoffDelay(
  streak: number,
  baseMs: number,
  retryAfterMs?: number | null,
): number {
  const safeStreak = Math.max(1, streak);
  const exp = baseMs * Math.pow(2, safeStreak - 1);
  const jitter = Math.random() * 250;
  const computed = Math.min(MAX_POLL_DELAY_MS, exp) + jitter;
  return clampPollDelay(computed, retryAfterMs);
}

/**
 * AC-FE-POLL-PROD-1: single-flight polling. Module-scoped controller is aborted
 * on reset() or when a new beginPolling() supersedes the previous one.
 */
let activePollController: AbortController | null = null;

function abortActivePoll(reason?: string): void {
  if (activePollController) {
    try {
      activePollController.abort(reason ?? 'superseded');
    } catch {
      /* noop */
    }
    activePollController = null;
  }
}

export function __getActivePollControllerForTest(): AbortController | null {
  return activePollController;
}

type QuizStatus = 'idle' | 'loading' | 'active' | 'finished' | 'error';
type QuizView = 'idle' | 'synopsis' | 'question' | 'result' | 'error';

/**
 * Deep-review #14 — the synopsis screen is generated by a PAID agent run, but
 * `/quiz/status` has no synopsis shape (only processing/question/result). So a
 * synopsis-phase refresh cannot be recovered by polling — recovery would poll
 * `processing` into a fatal 60s-timeout loop and discard the paid synopsis +
 * characters. We instead persist the synopsis `viewData` in the session
 * snapshot and restore it LOCALLY on recovery, resuming at the synopsis screen
 * with zero network calls.
 *
 * The extra `synopsis` field rides on top of the base `QuizStateSnapshot`
 * (session.ts persists/returns the JSON opaquely, preserving unknown keys), so
 * no change to `session.ts` is needed.
 */
interface PersistedSnapshot extends QuizStateSnapshot {
  synopsis?: (Synopsis & { characters?: unknown }) | null;
  /**
   * "Try a different interpretation" (2026-07-02): the original typed topic +
   * the interpretation readings rejected so far. Persisted with the synopsis
   * snapshot so the reload affordance survives a refresh (session.ts stores
   * the JSON opaquely, preserving unknown keys — no session.ts change needed).
   */
  category?: string | null;
  rejectedInterpretations?: string[];
}

interface QuizState {
  status: QuizStatus;
  currentView: QuizView;
  viewData: Synopsis | Question | ResultProfileData | null;

  quizId: string | null;

  knownQuestionsCount: number;
  answeredCount: number;

  totalTarget: number;

  uiError: string | null;
  /**
   * Whimsical-error-system (2026-06-30) — the precise `QF-...` code for the
   * current fatal error (when the backend supplied one), shown as light-grey
   * small text below the message. Null when there is no error or no code.
   */
  uiErrorCode: string | null;
  /** Backend-echoed trace id for the current error (support log correlation). */
  uiErrorTraceId: string | null;
  isSubmittingAnswer: boolean;

  isPolling: boolean;
  retryCount: number;
  /**
   * AC-FE-RELY-POLL-1/2 — count of consecutive 5xx / network failures during
   * polling. Reset to 0 on every successful poll. Used to scale exponential
   * backoff and to decide when to give up (>= MAX_RETRIES).
   */
  pollFailureStreak: number;
  lastPersistTime: number;
  sessionRecovered: boolean;

  /**
   * Blackbox fix #4(b) — async image URLs persisted in the store so they SURVIVE
   * a view change (synopsis → question → result). Previously the media snapshot
   * lived only inside the synopsis-scoped `useQuizMedia` hook, so late-arriving
   * cast images were discarded when the user proceeded — the cast "never loaded"
   * by the time the result page rendered. The store is the single durable home
   * for these, merged additively as the poller resolves them; URLs are never
   * overwritten with null once known.
   */
  characterImages: Record<string, string>;
  synopsisImageUrl: string | null;
  resultImageUrl: string | null;

  /**
   * "Try a different interpretation" (owner blackbox, 2026-07-02) — the
   * original typed topic for the current quiz flow. Needed so the synopsis
   * screen's subtle reload affordance can start a NEW quiz for the SAME topic
   * with the prior reading(s) rejected.
   */
  category: string | null;
  /**
   * Interpretation readings ("<title> — <summary>") the user has rejected for
   * `category` so far in this flow. Accumulates across reload cycles and is
   * sent with each reinterpret so the planner never repeats a rejected one.
   */
  rejectedInterpretations: string[];
  /** In-flight guard for the reinterpret call (blocks double-clicks). */
  isReinterpreting: boolean;
}

interface QuizActions {
  startQuiz: (category: string, turnstileToken: string) => Promise<void>;
  /**
   * "Try a different interpretation" (2026-07-02) — start a NEW quiz for the
   * SAME typed topic, rejecting the currently displayed synopsis reading (and
   * every previously rejected one). Shows the normal loading state; on arrival
   * the new synopsis replaces the old. Errors restore the previous synopsis
   * with an inline message — never a dead end.
   */
  reinterpret: (turnstileToken: string) => Promise<void>;
  hydrateFromStart: (payload: {
    quizId: string;
    initialPayload: InitialPayload;
    charactersPayload?: { type: 'characters'; data: any[] } | null;
  }) => void;
  hydrateStatus: (dto: api.QuizStatusDTO) => void;
  beginPolling: (options?: { reason?: string }) => Promise<void>;
  markAnswered: () => void;
  submitAnswerStart: () => void;
  submitAnswerEnd: () => void;
  setError: (
    message: string,
    isFatal?: boolean,
    meta?: { code?: string | null; traceId?: string | null },
  ) => void;
  clearError: () => void;
  recover: () => void;
  reset: () => void;
  persistToSession: () => void;
  recoverFromSession: () => Promise<boolean>;
  /**
   * Blackbox fix #4(b) — merge a media snapshot's resolved image URLs into the
   * store. Additive only: a URL is set once known and never cleared by a later
   * snapshot that lacks it, so late-arriving cast images survive a view change
   * and show through to the result page.
   */
  mergeMediaSnapshot: (snapshot: {
    synopsisImageUrl?: string | null;
    resultImageUrl?: string | null;
    characters?: ReadonlyArray<{ name: string; imageUrl: string | null }>;
  } | null) => void;
}

type QuizStore = QuizState & QuizActions;


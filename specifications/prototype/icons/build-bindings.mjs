// Generate a TS lookup (normalised text -> precomputed iconId) for the FE
// prototype, from routing/bindings.json. This stands in for the binding that
// production would carry INSIDE the pack/session payload (precomputed at
// build time; zero runtime routing).
//
// Run: node build-bindings.mjs
// Out: ../../../frontend/src/proto/qaBindings.ts

import { readFileSync, writeFileSync, mkdirSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const PROTO = join(__dirname, "..");
const OUT_DIR = join(__dirname, "..", "..", "..", "frontend", "src", "proto");
const OUT = join(OUT_DIR, "qaBindings.ts");

if (!existsSync(OUT_DIR)) mkdirSync(OUT_DIR, { recursive: true });

const data = JSON.parse(readFileSync(join(PROTO, "data", "bindings.json"), "utf8"));
const norm = (s) => s.toLowerCase().replace(/\s+/g, " ").trim();

const map = {};
for (const b of data.bindings) {
  if (b.iconId) map[norm(b.text)] = b.iconId;
}

const ts = `// AUTO-GENERATED by specifications/prototype/icons/build-bindings.mjs — DO NOT EDIT.
// Precomputed { question/answer text -> iconId } bindings from the semantic
// router (backend=${data.backend}, tau=${data.tau}). In production this binding
// lives in the pack/session payload; here it is a static lookup so the FE does
// ZERO routing at runtime. Gated behind VITE_PROTO_QA_ICONS via QaIcon.
/* eslint-disable */
const BINDINGS: Record<string, string> = ${JSON.stringify(map, null, 2)};

export function protoIconForText(text: string | undefined | null): string | null {
  if (!text) return null;
  const key = text.toLowerCase().replace(/\\s+/g, ' ').trim();
  return BINDINGS[key] ?? null;
}
`;

writeFileSync(OUT, ts, "utf8");
console.log(`wrote ${Object.keys(map).length} bindings -> ${OUT}`);
